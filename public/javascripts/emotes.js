var emoteLimit = 4;

var checkEmotes = function(msg) {
	var words = msg.split(" ");
    var emoteCount = 0;
	var fullMessage = "";
	for (word in words) {
		if (words[word] in emotes) {
            if (emoteLimit > emoteCount) {
                words[word] = "<img src='" + emotes[words[word]] + "' height='50'>";
                emoteCount++;
            };
		}
		if (fullMessage) {
			fullMessage += " " + words[word];
		} else{
			fullMessage += words[word];
		}
	}
	return fullMessage;
}